    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (locacao == null)
                locacao = new Locacao();
            
            Jogo produto = (Jogo) cmbJogo.getSelectedItem();
            double valor = Double.parseDouble(txtValor.getText().replace(",", "."));
            LocalDateTime data = LocalDateTime.parse(txtData.getText(), Util.getDateTimeF());
            LocalDateTime devolucao = LocalDateTime.parse(txtDevolucao.getText(), Util.getDateTimeF());
            
            if (produto.isLocado()) {
                locacao = null;
                JOptionPane.showMessageDialog(rootPane, "Ops, este jogo já está locado!");
                return;
            }
            
            locacao.setCliente((Cliente) cmbCliente.getSelectedItem());
            locacao.setProduto(produto);
            locacao.setVendedor((Funcionario) cmbFuncionario.getSelectedItem());
            locacao.setValor(valor);
            locacao.setDataLocacao(data);
            locacao.setPrazo(Integer.parseInt(txtPrazo.getText()));
            locacao.setDataDevolucao(devolucao);
            locacao.setPgto((FormaPGTO) cmbPGTO.getSelectedItem());
            
            locacao.getProduto().setLocado(devolucao.isAfter(LocalDateTime.now()));
            daoJogo.persist(locacao.getProduto());
            
            this.dispose();
        } catch (Exception e) {
            locacao = null;
            JOptionPane.showMessageDialog(rootPane, "Algo deu errado!\n" + e);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void txtPrazoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPrazoActionPerformed
        LocalDateTime data = LocalDateTime.parse(txtData.getText(), Util.getDateTimeF()).withHour(23).withMinute(59).withSecond(59).withNano(0);
        data = data.plusDays(Long.parseLong(txtPrazo.getText()));
        txtDevolucao.setText(Util.formatDateTime(data));
    }//GEN-LAST:event_txtPrazoActionPerformed

