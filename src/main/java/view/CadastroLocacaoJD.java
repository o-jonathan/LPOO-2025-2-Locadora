    public void loadClientes() {
        for (Cliente c: daoCliente.listaClientes()) {
            cmbCliente.addItem(c);
        }
    }
    
    public void loadFuncionarios() {
        for (Funcionario f: daoFuncionario.listaFuncionarios()) {
            cmbFuncionario.addItem(f);
        }
    }
    
    public void loadJogos() {
        for (Jogo j: daoJogo.listaJogos()) {
            cmbJogo.addItem(j);
        }
    }
    
    public void loadPGTO() {
        for (FormaPGTO pgto: FormaPGTO.values()) {
            cmbPGTO.addItem(pgto);
        }
    }
    
